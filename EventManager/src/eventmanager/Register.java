    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        try{
            if(cb1.isSelected()==true)
            {
                String str=new String(txtDeveloper.getPassword());
                if(str.equals("0000")==false)
                {
                    throw new Exception("Developer's Passwrod Incorrect");
                }
            }
            Socket s=new Socket(add,port);
            DataOutputStream dout=new DataOutputStream(s.getOutputStream());
            dout.writeUTF("Signup");
            dout.writeUTF(txtName.getText());
            dout.writeUTF(psswdNew.getText());
            dout.flush();
            DataInputStream din=new DataInputStream(s.getInputStream());
            JOptionPane.showMessageDialog(null,din.readUTF());
            din.close();
            dout.close();
            s.close();
            Login_Form lf=new Login_Form();
            lf.setVisible(true);
            this.setVisible(false);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

